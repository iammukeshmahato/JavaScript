// trim() method is used to remove the starting and ending white space from the string

let str = "     hello world!  ";
console.log(str.trim());

// trimStart() is used to remove the white space from the starting
str  = "         hello world!       ";
console.log(str.trimStart());

// trimEnd() is used to remove the white space from the Ending
str  = "         hello world!       ";
console.log(str.trimEnd());     //      hello world!